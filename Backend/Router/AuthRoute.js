module.exports = (app) => {

    const auth = require('../Controllers/Auth');
    const router = require('express').Router();

    /**------------------------------------------------------------------------------
     * RUTAS DE CARGOS
     * ------------------------------------------------------------------------------
     */
    router.post('/', auth.login);


    /**------------------------------------------------------------------------------
     * FIN DE RUTAS DE CARGOS
     * ------------------------------------------------------------------------------
     */

    app.use('/api/auth', router);

}